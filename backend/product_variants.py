def expand_variants(base: dict) -> list[dict]:
    """根据规则把一个 base 商品展开为多个 SKU 变体。没配置时返回 base 自身。"""
    specs = VARIANT_SPECS.get(base["id"])
    if not specs:
        return [dict(base)]
    results: list[dict] = []
    for key, prefix, delta, stock, desc_suffix in specs:
        v = dict(base)
        v["id"] = f"{base['id']}-{key}"
        v["base_id"] = base["id"]
        v["variant_key"] = key
        v["name"] = f"{prefix} · {base['name']}"
        v["price"] = max(1.0, float(base["price"]) + float(delta))
        v["stock"] = int(stock)
        v["description"] = f"{base['description']} {desc_suffix}"
        v["tags"] = (base.get("tags") or []) + [prefix.strip()]
        results.append(v)
    return results


def total_variant_count() -> int:
    """仅用于自检/统计。"""
    return sum(len(v) for v in VARIANT_SPECS.values())
